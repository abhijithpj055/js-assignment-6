//1.Math.abs()
const val1 = Math.abs(2-3);
console.log('Absolute value:', val1);

// 2.Math.ceil()
const cvalue = Math.ceil(5.6);
console.log('Ceil:', cvalue);

// 3.Math.floor()
const fvalue = Math.floor(3.4)
console.log('Floor value:', fvalue);

// 4.Math.round()
const rvalue = Math.round(2.6);
console.log('Round:', rvalue);

// 5.Math.Max()
const max = Math.max(5,10);
console.log('Max:', max);

// 6.Math.min()
const min = Math.min(2,12);
console.log('Min:', min);

// 7.Math.pow()
const pvalue = Math.pow(2,5); 
console.log('Power:', pvalue);

// 8.Math.sqrt()
const sqvalue = Math.sqrt(36);
console.log('Square root:', sqvalue);

// 9.Math.trunc() 
const tvalue = Math.trunc(1.20);
console.log('Truncate:', tvalue);

// 10.Math.random()
const rnum = Math.random();
console.log('Random number:', rnum);